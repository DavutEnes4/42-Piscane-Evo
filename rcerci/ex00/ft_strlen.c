int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

#include <stdio.h>
int	main(void)
{
	printf("%d",ft_strlen("0123456789101112131415161718192021"));
}
